 void encender_led_verde(){
	 //encender led verde
	 GPIO_PinWrite(GPIOD, 5, 0);
 }

 void apagar_led_verde(){
	 //apagar led verde
	 GPIO_PinWrite(GPIOD, 5, 1);
 }

 void toggle_led_rojo(){
	 //encender led rojo
	 GPIO_PortToggle(GPIOE, 1U<<31);
 }
